    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
       
        String nombre = txtnombre.getText();
        int year = Integer.parseInt(aniotxt.getText());
        int precio = Integer.parseInt(preciotxt.getText());
        String genero = generocb.getItemAt(generocb.getSelectedIndex());
        
        MiPelicula pelicula = new MiPelicula(nombre, genero, year, precio);
        
        peliculas [index] = pelicula;
        JOptionPane.showMessageDialog(null,"Se ha agregado su pelicula");
        txtnombre.setText("");  //Esta parte de codigo reinicia los valores del gui
        aniotxt.setText("");
        preciotxt.setText("");
        generocb.setSelectedIndex(0);
        index++;
    }//GEN-LAST:event_jButton2ActionPerformed

