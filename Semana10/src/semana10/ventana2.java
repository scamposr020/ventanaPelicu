    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
            
         String buscaInfo= txtnombre.getText();
             for (int i = 0; i < peliculas.length; i++) {
                    
                if(peliculas[i]!=null){
                    if (peliculas[i].getNombre().equals(buscaInfo)){
                        aniotxt.setText(String.valueOf(peliculas[i].getYear()));
                        preciotxt.setText(String.valueOf(peliculas[i].getPrecio()));
                        generocb.setSelectedItem(peliculas[i].getGenero());
                    }
                    if(!peliculas[i].getNombre().equals(buscaInfo)){
                        if(i==index-1){
                       JOptionPane.showMessageDialog(null, "No se encontro nada");
                       txtnombre.setText("");
                        }
                    }
                }
            }

        
    }//GEN-LAST:event_jButton1ActionPerformed

